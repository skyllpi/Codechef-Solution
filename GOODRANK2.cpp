#include <bits/stdc++.h>
using namespace std;

int main() {
	
    int n;
    cin>>n;
    int a = 1;
    int k = n;
    cout<<2<<endl;
    while(n--)
    {
        cout<<a<<" ";
        a++;
    }
    cout<<endl;
    --a;
    while(k--)
    {
        cout<<a<<" ";
        a--;
    }
    cout<<endl;
    
    
}
